function parseTablero(tablero){
    //console.log(tablero);
    var tableroAsArray = [
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
        ["","","","","","","","","","","","","","","",""],
    ]

    for(let j=0;j<=15;j++){
        let quinceCaracteres = tablero.slice(j*16,j*16+16)
        for(let i=0;i<quinceCaracteres.length;i++){
            tableroAsArray[j][i] = quinceCaracteres[i]
        }
    }
    return tableroAsArray
}

module.exports = parseTablero;